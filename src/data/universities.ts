/**
 * The full catalogue: every curated rich record (flagged `curated`) first,
 * then the comprehensive generated index of all remaining universities so that
 * each country shows its complete list — not just the flagship handful.
 */
export const universities: University[] = [
  ...curatedUniversities.map((u) => ({
    ...u,
    curated: true,
    // Curated records get their main-field tags from the structured eligibility data.
    disciplines: eligibility[u.slug]?.disciplines ?? u.disciplines,
  })),
  ...generatedUniversities,
];

/** Only curated records get an in-app detail page. */
export const curatedSlugs = new Set(curatedUniversities.map((u) => u.slug));

export function getUniversity(slug: string): University | undefined {
  return universities.find((u) => u.slug === slug);
}

export function universitiesByCountry(code: string): University[] {
  return universities.filter((u) => u.country === code);
}
